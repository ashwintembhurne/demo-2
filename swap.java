package day2;

public class swap {

	public static void main(String[] args) {
	int a=10;
	int b=30;
	System.out.println("before swapping "+a+"  "+b);
	System.out.println("____________________________________________");
	int temp=a;
	
	temp=a;
	a=b;
	b=temp;
	System.out.println(" \n after swapping "+a+"  "+b);

	}

}
